    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession httpSession = request.getSession();

        if (httpSession.getAttribute("User") != null) {
            String userName = (String) httpSession.getAttribute("UserName");
            List<Product> productList = ProductDAO.reStore_AllProduct_FromFile();
            httpSession.setAttribute("ListProduct", productList);
            UserCart currentUserCart = UserCartManager.findUserCart(userName); 
            httpSession.setAttribute("UserName",userName);
            httpSession.setAttribute("userCart", currentUserCart.getUserCart());
            response.sendRedirect("shopping-page_LoginSuccess.jsp");
        } else {
            List<Product> productList = ProductDAO.reStore_AllProduct_FromFile();
            httpSession.setAttribute("ListProduct", productList);
            response.sendRedirect("shopping-page.jsp");
        }
    }

